package com.example.demo.interceptor;

public class Pair {

    Long first;
    Long second;

    public Pair(Long first, Long second) {
        this.first = first;
        this.second = second;
    }
    public Long getFirst() {
        return first;
    }

    public void setFirst(Long first) {
        this.first = first;
    }

    public Long getSecond() {
        return second;
    }

    public void setSecond(Long second) {
        this.second = second;
    }

}
